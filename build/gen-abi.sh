#!/usr/bin/env bash
# Usage: build/gen-abi.sh [--check]
#
# (Re)generates the link-time ABI handshake files in the Go packages. The
# root package calls cv2_abi_<hash>() and the f2d subpackage calls
# cv2_f2d_abi_<hash>(); the generated libs modules define those symbols
# (see package-libs.sh). A consumer that mixes a root module with libs
# from a different wrapper generation gets an undefined-reference link
# error naming the expected hash, instead of undefined runtime behavior.
#
# --check regenerates to a temp dir and fails if the committed files are
# stale (used by CI).
set -euo pipefail
source "$(dirname "${BASH_SOURCE[0]}")/lib.sh"

hash=$(cv2_abi_hash)
tag='//go:build (linux && (amd64 || 386 || arm64)) || (windows && (amd64 || 386)) || (darwin && arm64)'

emit_root() {
  cat <<EOF
$tag

// Code generated by build/gen-abi.sh. DO NOT EDIT.
//
// Link-time pairing check: the prebuilt base libs module for this wrapper
// generation defines cv2_abi_$hash. Mixing this source version with libs
// from another generation fails to link, naming the expected hash.
package cv2

// extern void cv2_abi_$hash(void);
import "C"

func init() {
	C.cv2_abi_$hash()
}
EOF
}

emit_f2d() {
  cat <<EOF
$tag

// Code generated by build/gen-abi.sh. DO NOT EDIT.
//
// Link-time pairing check against the f2d feature-set libs module; see
// the root package's counterpart for the rationale.
package f2d

// extern void cv2_f2d_abi_$hash(void);
import "C"

func init() {
	C.cv2_f2d_abi_$hash()
}
EOF
}

if [ "${1:-}" = "--check" ]; then
  if ! diff -u <(emit_root) "$CV2_ROOT/zz_abi_link.go" ||
    ! diff -u <(emit_f2d) "$CV2_ROOT/f2d/zz_abi_link.go"; then
    echo "error: ABI handshake files are stale; run build/gen-abi.sh and commit" >&2
    exit 1
  fi
  echo "ABI handshake files up to date (generation $hash)"
else
  emit_root >"$CV2_ROOT/zz_abi_link.go"
  emit_f2d >"$CV2_ROOT/f2d/zz_abi_link.go"
  echo "==> ABI generation $hash written"
fi
