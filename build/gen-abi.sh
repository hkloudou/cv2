#!/usr/bin/env bash
# Usage: build/gen-abi.sh [--check]
#
# (Re)generates the link-time ABI handshake files in the Go packages. The
# root package calls cv2_abi_<hash>() and every feature subpackage <set>
# calls cv2_<set>_abi_<hash>(); the generated libs modules define those
# symbols (see package-libs.sh). A consumer that mixes a root module with
# libs from a different wrapper generation gets an undefined-reference
# link error naming the expected hash, instead of undefined runtime
# behavior.
#
# --check regenerates to a temp stream and fails if the committed files
# are stale (used by CI).
set -euo pipefail
source "$(dirname "${BASH_SOURCE[0]}")/lib.sh"

# shellcheck disable=SC1091
source "$CV2_ROOT/build/build.conf"

hash=$(cv2_abi_hash)
tag='//go:build (linux && (amd64 || 386 || arm64)) || (windows && (amd64 || 386)) || (darwin && arm64)'

emit() {
  local pkg=$1 symbol=$2
  cat <<EOF
$tag

// Code generated by build/gen-abi.sh. DO NOT EDIT.
//
// Link-time pairing check: the prebuilt libs module for this wrapper
// generation defines $symbol. Mixing this source version with libs from
// another generation fails to link, naming the expected hash.
package $pkg

// extern void $symbol(void);
import "C"

func init() {
	C.$symbol()
}
EOF
}

check_or_write() {
  local pkg=$1 symbol=$2 path=$3
  if [ "$MODE" = check ]; then
    if ! diff -u <(emit "$pkg" "$symbol") "$path"; then
      echo "error: $path is stale; run build/gen-abi.sh and commit" >&2
      exit 1
    fi
  else
    emit "$pkg" "$symbol" >"$path"
  fi
}

MODE=${1:-write}
[ "$MODE" = "--check" ] && MODE=check

check_or_write cv2 "cv2_abi_$hash" "$CV2_ROOT/zz_abi_link.go"
for set in ${CV2_FEATURE_SETS:-}; do
  check_or_write "$set" "cv2_${set}_abi_$hash" "$CV2_ROOT/$set/zz_abi_link.go"
done

if [ "$MODE" = check ]; then
  echo "ABI handshake files up to date (generation $hash)"
else
  echo "==> ABI generation $hash written"
fi
