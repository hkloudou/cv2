#!/usr/bin/env bash
# Usage: build/package-libs.sh <target>
#
# Assembles the content of the prebuilt/<target> branch in .work/out/<target>:
#
#   MANIFEST                      build provenance + layer keys
#   README.md                     "machine generated" notice
#   sdk/                          OpenCV install tree (headers + libs), kept so
#                                 wrapper-only CI runs can skip the OpenCV build
#   libs/<goos>_<goarch>/         standalone Go module with the static libraries
#       go.mod libs.go MANIFEST LICENSE-OPENCV.txt *.a
set -euo pipefail
source "$(dirname "${BASH_SOURCE[0]}")/lib.sh"

cv2_load_target "${1:?usage: package-libs.sh <target>}"

module_dir_name=${TARGET_GOOS}_${TARGET_GOARCH}
module_path=github.com/hkloudou/cv2/libs/$module_dir_name
out_libs=$CV2_OUT_DIR/libs/$module_dir_name

pick_one() {
  # pick_one <what> <candidates...> - fail loudly unless exactly one match.
  local what=$1
  shift
  if [ "$#" -eq 0 ] || [ ! -e "$1" ]; then
    echo "error: no $what found" >&2
    exit 1
  fi
  if [ "$#" -gt 1 ]; then
    echo "error: multiple candidates for $what: $*" >&2
    exit 1
  fi
  printf '%s\n' "$1"
}

core_lib=$(pick_one "libopencv_core" $(find "$CV2_DIST_DIR" -name 'libopencv_core*.a' | LC_ALL=C sort))
imgproc_lib=$(pick_one "libopencv_imgproc" $(find "$CV2_DIST_DIR" -name 'libopencv_imgproc*.a' | LC_ALL=C sort))
# The dist tree may hold identical zlib copies in more than one place
# (3rdparty-lib/ plus the platform's staticlib dir); any one of them works.
zlib_lib=$(find "$CV2_DIST_DIR" -name 'libzlib.a' | LC_ALL=C sort | head -1)
[ -n "$zlib_lib" ] || { echo "error: no libzlib.a found in $CV2_DIST_DIR" >&2; exit 1; }
wrapper_lib=$CV2_OBJ_DIR/libcv2wrapper.a
[ -f "$wrapper_lib" ] || { echo "error: $wrapper_lib missing (run build-wrapper.sh first)" >&2; exit 1; }

rm -rf "$CV2_OUT_DIR"
mkdir -p "$out_libs"

# Normalize names so the cgo link flags are identical on every platform
# (MinGW builds produce version-suffixed archives like libopencv_core481.a).
cp "$core_lib" "$out_libs/libopencv_core.a"
cp "$imgproc_lib" "$out_libs/libopencv_imgproc.a"
cp "$zlib_lib" "$out_libs/libzlib.a"
cp "$wrapper_lib" "$out_libs/libcv2wrapper.a"
cp "$CV2_DIST_DIR/OPENCV-LICENSE.txt" "$out_libs/LICENSE-OPENCV.txt"

opencv_key=$(cv2_build_key "$CV2_TARGET" opencv)
wrapper_key=$(cv2_build_key "$CV2_TARGET" wrapper)
# --verify --quiet keeps unborn branches from echoing a literal "HEAD".
source_commit=$(git -C "$CV2_ROOT" rev-parse --verify --quiet HEAD 2>/dev/null || true)
[ -n "$source_commit" ] || source_commit=unknown
built_at=$(date -u +%Y-%m-%dT%H:%M:%SZ)

cat >"$CV2_OUT_DIR/MANIFEST" <<EOF
TARGET=$CV2_TARGET
GOOS=$TARGET_GOOS
GOARCH=$TARGET_GOARCH
OPENCV_VERSION=$OPENCV_VERSION
OPENCV_MODULES=$OPENCV_MODULES
OPENCV_BUILD_KEY=$opencv_key
WRAPPER_KEY=$wrapper_key
SOURCE_COMMIT=$source_commit
BUILT_AT=$built_at
EOF
cp "$CV2_OUT_DIR/MANIFEST" "$out_libs/MANIFEST"

cat >"$out_libs/go.mod" <<EOF
module $module_path

go 1.24.0
EOF

cat >"$out_libs/libs.go" <<EOF
//go:build $TARGET_GOOS && $TARGET_GOARCH

// Package libs carries the prebuilt static libraries (OpenCV $OPENCV_VERSION,
// modules: $OPENCV_MODULES, plus zlib and the cv2 C wrapper) that
// github.com/hkloudou/cv2 links on $TARGET_GOOS/$TARGET_GOARCH.
//
// Generated by build/package-libs.sh. Do not edit.
package libs

/*
#cgo LDFLAGS: $LIBS_CGO_LDFLAGS
*/
import "C"
EOF

cat >"$CV2_OUT_DIR/README.md" <<EOF
# prebuilt/$CV2_TARGET

Machine-generated branch. Do not edit or commit here by hand.

This branch is force-pushed as a single commit by the \`build-libs\` GitHub
Actions workflow of [github.com/hkloudou/cv2](https://github.com/hkloudou/cv2).
It carries the prebuilt static libraries for $TARGET_GOOS/$TARGET_GOARCH:

- \`libs/$module_dir_name/\` - the Go module \`$module_path\`
  (release tags \`libs/$module_dir_name/vX.Y.Z\` point into this branch)
- \`sdk/\` - the OpenCV install tree, kept so wrapper-only rebuilds can skip
  compiling OpenCV
- \`MANIFEST\` - build provenance and layer cache keys
EOF

# Keep the full SDK for incremental wrapper rebuilds.
cp -a "$CV2_DIST_DIR" "$CV2_OUT_DIR/sdk"

echo "==> Packaged $CV2_OUT_DIR"
du -sh "$CV2_OUT_DIR" "$out_libs"
ls -la "$out_libs"
